export function fmtVal(base, pct, pre='') {
  const v = base * (1 + pct / 100);
  if (v >= 10000) return pre + v.toLocaleString('en-IN', {maximumFractionDigits:0});
  if (v >= 100) return pre + v.toLocaleString('en-IN', {maximumFractionDigits:2});
  return pre + v.toFixed(2);
}

export function fmtChg(pct) {
  if (pct > 0) return {txt:`▲ +${pct.toFixed(2)}%`, cls:'chg-up'};
  if (pct < 0) return {txt:`▼ ${pct.toFixed(2)}%`,  cls:'chg-dn'};
  return {txt:'→ 0.00%', cls:'chg-neu'};
}

